package Part1.Lesson2;

//Print 10 lines with a while loop.
public class WhileLoop {
//    Print 10 lines: "Task1". “Task2”. … “Task10”. Use the while loop.
    public static void main(String[] args) {
        int i = 1;
        while(i <= 10){
            System.out.println("Task" + i);
            i++;
        }
    }
}
